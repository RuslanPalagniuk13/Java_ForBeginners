/* Урок 3:
 * Строки (String)
 */

public class lesson3 {
    public static void main(String[] args) {
        int x = 13;
        String s = "Привет"; // String относится к классу и пишится с большой боквы
        String space = " ";
        String name = "Ксюша";
        System.out.println(s+space+name); // ввыводит строки по заданным переменным строкам
        System.out.println("Привет" + " " + "Настя");
        System.out.println("Мой номер " + x);
    }
}